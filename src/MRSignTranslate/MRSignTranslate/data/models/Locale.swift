import Foundation

enum LocaleCountry: String, CaseIterable {
    case argentina = "es_AR"
    case america = "en_US"
    case germany = "de_DE"
    case france = "fr_FR"
    case britain = "en_GB"
    case switzerlandGerman = "de_CH"
    case switzerlandFrench = "fr_CH"
    case switzerlandItalian = "it_CH"
    case israel = "he_IL"
    case spain = "es_ES"
    case jordan = "ar_JO"
    case belarusBelarusian = "be_BY"
    case belarusRussian = "ru_BY"
    case bulgaria = "bg_BG"
    case china = "zh_CN"
    case croatia = "hr_HR"
    case czechRepublic = "cs_CZ"
    case denmark = "da_DK"
    case indiaEnglish = "en_IN"
    case indiaHindi = "hi_IN"
    case indiaBengali = "bn_IN"
    case indiaTamil = "ta_IN"
    case indiaTelugu = "te_IN"
    case newZealand = "en_NZ"
    case estonia = "et_EE"
    case finland = "fi_FI"
    case austria = "de_AT"
    case cyprusGreek = "el_CY"
    case cyprusTurkish = "tr_CY"
    case greece = "el_GR"
    case iceland = "is_IS"
    case italy = "it_IT"
    case japan = "ja_JP"
    case latvia = "lv_LV"
    case lithuania = "lt_LT"
    case iran = "fa_IR"
    case poland = "pl_PL"
    case brazil = "pt_BR"
    case portugal = "pt_PT"
    case russia = "ru_RU"
    case slovakia = "sk_SK"
    case chile = "es_CL"
    case cuba = "es_CU"
    case mexico = "es_MX"
    case sweden = "sv_SE"
    case turkey = "tr_TR"
    case ukraine = "uk_UA"
    case pakistanUrdu = "ur_PK"
    case pakistanEnglish = "en_PK"
    
    /// Returns the display name of the country
    var displayName: String {
        switch self {
        case .argentina: return "Argentinian"
        case .america: return "American"
        case .germany: return "German"
        case .france: return "French"
        case .britain: return "British"
        case .switzerlandGerman: return "Swiss-German"
        case .switzerlandFrench: return "Swiss-French"
        case .switzerlandItalian: return "Swiss-Italian"
        case .israel: return "Israeli"
        case .spain: return "Spanish"
        case .jordan: return "Jordanian"
        case .belarusBelarusian, .belarusRussian: return "Belarussian"
        case .bulgaria: return "Bulgarian"
        case .china: return "Chinese"
        case .croatia: return "Croatian"
        case .czechRepublic: return "Czech"
        case .denmark: return "Danish"
        case .indiaEnglish, .indiaHindi, .indiaBengali, .indiaTamil, .indiaTelugu: return "Indian"
        case .newZealand: return "New Zealandian"
        case .estonia: return "Estonian"
        case .finland: return "Finnish"
        case .austria: return "Austrian"
        case .cyprusGreek, .cyprusTurkish: return "Cypriot"
        case .greece: return "Greek"
        case .iceland: return "Icelandic"
        case .italy: return "Italian"
        case .japan: return "Japanese"
        case .latvia: return "Latvian"
        case .lithuania: return "Lithuanian"
        case .iran: return "Persian"
        case .poland: return "Polish"
        case .brazil: return "Brazilian"
        case .portugal: return "Portugese"
        case .russia: return "Russian"
        case .slovakia: return "Slovakian"
        case .chile: return "Chilian"
        case .cuba: return "Cuban"
        case .mexico: return "Mexican"
        case .sweden: return "Swedish"
        case .turkey: return "Turkish"
        case .ukraine: return "Ukrainian"
        case .pakistanUrdu, .pakistanEnglish: return "Pakistani"
        }
    }
}
